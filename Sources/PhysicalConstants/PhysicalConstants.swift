
public enum PhysicalConstants {
    
    // Newtonian constant of gravitation (m³ kg⁻¹ s⁻²)
    public static let newtonianGravitationalConstant: Double = 6.674_30e-11
    
    // The Planck constant  (J Hz⁻¹)
    public static let planckConstant: Double = 6.626_070_15e-34
    
    // The reduced Planck constant in (J s)
    public static let reducedPlanckConstant: Double = planckConstant / (2 * Double.pi)
    
    // The speed of light in vacuum (m s⁻¹)
    public static let speedOfLight: Double = 299_792_458
    
    // The vacuum electric permittivity (F m⁻¹)
    public static let vacuumElectricPermittivity: Double = 8.854_187_8188e-12
    
    // The vacuum magnetic permeability (N A⁻²)
    public static let vacuumMagneticPermeability: Double = 1.256_637_061_27e-6
    
    // The charge of an electron (C)
    public static let elementaryCharge: Double = 1.602_176_634e-19
    
    // The mass of an electron (kg)
    public static let electronMass: Double = 9.109_383_7139e-31
    
    // The Avogadro constant (mol⁻¹)
    public static let avogadroConstant: Double = 6.022_140_76e23
    
    // The Boltzmann constant (J K⁻¹)
    public static let boltzmannConstant: Double = 1.380_649e-23
    
    // The standard acceleration of gravitation (m s⁻²)
    public static let standardGravityAcceleration: Double = 9.806_65
    
}
